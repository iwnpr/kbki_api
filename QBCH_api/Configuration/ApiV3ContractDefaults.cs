namespace QBCH_api.Configuration;

/// <summary>
/// Канонические лимиты и таймауты контракта API 3.0.
/// Используются как безопасные значения по умолчанию, если конфигурация не задана.
/// </summary>
public static class ApiV3ContractDefaults
{
    /// <summary>
    /// Максимальное количество блоков запроса, допустимое в одном пакетном запросе <c>/dlrequest</c>.
    /// По контракту API 3.0 в одном пакете допускается не более 10 запросов по субъектам.
    /// </summary>
    public const int MaxDlRequestBatchSize = 10;

    /// <summary>
    /// Максимальное количество сущностей, допустимое в одном запросе <c>/dlput</c>.
    /// Ограничение суммарно применяется к договорам и обращениям/обязательствам.
    /// </summary>
    public const int MaxDlPutEntities = 1000;

    /// <summary>
    /// Минимальный интервал между повторными запросами результата после получения ошибки «Ответ не готов».
    /// Значение задаётся в секундах.
    /// </summary>
    public const int MinAnswerPollingIntervalSeconds = 1;

    /// <summary>
    /// Минимальное время хранения готового ответа по идентификатору ответа.
    /// Значение задаётся в часах.
    /// </summary>
    public const int ResponseRetentionHours = 8;

    /// <summary>
    /// Предельное время, в течение которого сервис пытается вернуть готовый результат сразу,
    /// без перехода к асинхронной модели с квитанцией и идентификатором ответа.
    /// Значение задаётся в секундах.
    /// </summary>
    public const int ImmediateResponseDeadlineSeconds = 4;

    /// <summary>
    /// Таймаут исходящих HTTP-вызовов к другим КБКИ.
    /// Значение задаётся в секундах и синхронизируется с бюджетом времени на немедленный ответ.
    /// </summary>
    public const int HttpClientTimeoutSeconds = 4;
}
